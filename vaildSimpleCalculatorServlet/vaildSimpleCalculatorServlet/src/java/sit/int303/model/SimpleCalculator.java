package sit.int303.model;

/**
 *
 * @author INT303
 */
public class SimpleCalculator {
    private double x , y;
    private String op ;

    public double getResult(){
        switch (op){
            case "+":
                return x + y;
            case "-":
                return x - y;
            case "*":
                return x * y;
            case "/":
                return x / y;
            default:
            return 0;
        }
    }
    
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public String getOp() {
        return op;
    }

    public void setOp(String op) {
        this.op = op;
    }

    @Override
    public String toString() {
        return String.format("x =%f, y= %f %f %s %f= %f</h1>\n", x,y,x,op,y,getResult());
    }
    
}
